/**
 * @param {number[][]} A
 * @return {number}
 */
var shortestBridge = function (A) {
  const m = A.length;
  if (m === 0) return;
  const n = A[0].length;
  const g = (i, j) => {
    if (i < 0 || j < 0 || i >= m || j >= n) return -1;
    return i * m + j;
  };
  const get = (g) => {
    return { i: Math.floor(g / m), j: g % m };
  };
  let queue = [];
  let idx = 0;
  let f = false;
  for (let i = 0; i < m; i++) {
    for (let j = 0; j < n; j++) {
      if (A[i][j] === 1) {
        queue.push(g(i, j));
        f = true;
        break;
      }
    }
    if (f) break;
  }
  const visited = [];
  while (idx < queue.length) {
    const id = queue[idx++];
    const { i, j } = get(id);
    A[i][j] = 2;
    if (i > 0 && A[i - 1][j] === 1 && !visited[g(i - 1, j)]) {
      queue.push(g(i - 1, j));
      visited[g(i - 1, j)] = true;
    }
    if (j > 0 && A[i][j - 1] === 1 && !visited[g(i, j - 1)]) {
      queue.push(g(i, j - 1));
      visited[g(i, j - 1)] = true;
    }
    if (i < m - 1 && A[i + 1][j] === 1 && !visited[g(i + 1, j)]) {
      queue.push(g(i + 1, j));
      visited[g(i + 1, j)] = true;
    }
    if (j < n - 1 && A[i][j + 1] === 1 && !visited[g(i, j + 1)]) {
      queue.push(g(i, j + 1));
      visited[g(i, j + 1)] = true;
    }
  }
  queue = [];
  for (let i = 0; i < m; i++) {
    for (let j = 0; j < n; j++) {
      if (A[i][j] === 2) queue.push(g(i, j));
    }
  }
  idx = 0;
  let step = queue.length;
  let ans = 0;
  while (idx < queue.length) {
    while (idx < step) {
      const id = queue[idx++];
      const { i, j } = get(id);
      if (i > 0 && A[i - 1][j] === 0 && !queue.includes(g(i - 1, j))) {
        queue.push(g(i - 1, j));
        A[i - 1][j] = 2;
      } else if (i > 0 && A[i - 1][j] === 1) {
        return ans;
      }
      if (i < m - 1 && A[i + 1][j] === 0 && !queue.includes(g(i + 1, j))) {
        queue.push(g(i + 1, j));
        A[i + 1][j] = 2;
      } else if (i < m - 1 && A[i + 1][j] === 1) {
        return ans;
      }
      if (j > 0 && A[i][j - 1] === 0 && !queue.includes(g(i, j - 1))) {
        queue.push(g(i, j - 1));
        A[i][j - 1] = 2;
      } else if (j > 0 && A[i][j - 1] === 1) {
        return ans;
      }
      if (j < n - 1 && A[i][j + 1] === 0 && !queue.includes(g(i, j + 1))) {
        queue.push(g(i, j + 1));
        A[i][j + 1] = 2;
      } else if (j < n - 1 && A[i][j + 1] === 1) {
        return ans;
      }
    }
    step = queue.length;
    ans++;
  }
};
